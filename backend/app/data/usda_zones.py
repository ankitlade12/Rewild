def get_zone(zip_code: str) -> dict:
    """Look up USDA Hardiness Zone info for a US zip code.
    
    Returns dict with: zone, min_temp_f_low, min_temp_f_high,
    last_frost, first_frost, state
    """
    prefix = zip_code[:3]
    
    zone_data = ZIP_TO_ZONE.get(prefix)
    if not zone_data:
        return {"error": f"Unknown zip prefix: {prefix}", "zone": None}
    
    zone, temp_low, temp_high = zone_data
    frost = ZONE_FROST_DATES.get(zone, ("Unknown", "Unknown"))
    state = ZIP_TO_STATE.get(prefix, "Unknown")
    
    return {
        "zone": zone,
        "min_temp_f_low": temp_low,
        "min_temp_f_high": temp_high,
        "last_frost": frost[0],
        "first_frost": frost[1],
        "state": state,
    }
